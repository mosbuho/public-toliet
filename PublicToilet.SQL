ALTER SESSION SET "_ORACLE_SCRIPT" = true;
CREATE USER PTUSER IDENTIFIED BY 123
DEFAULT TABLESPACE USERS
TEMPORARY TABLESPACE TEMP;
GRANT CONNECT, RESOURCE, DBA TO PTUSER;

DROP TABLE PUBLICTOILET;
DROP SEQUENCE PT_SEQ;

CREATE SEQUENCE PT_SEQ START WITH 1 INCREMENT BY 1;

CREATE TABLE PUBLICTOILET (
    PT_NO NUMBER DEFAULT PT_SEQ.NEXTVAL CONSTRAINT PT_PK PRIMARY KEY,
    PT_NAME VARCHAR2(255),
    PT_ADDR VARCHAR2(255),
    PT_MFUSE CHAR(1) CHECK (PT_MFUSE IN ('Y', 'N')),
    OPENTIME VARCHAR2(255),
    SOURCEDATE DATE
);

SELECT COUNT(*) FROM PUBLICTOILET;

DELETE FROM PUBLICTOILET;
